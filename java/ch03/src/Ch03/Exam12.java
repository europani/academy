package Ch03;

public class Exam12 {

	public static void main(String[] args) {
		System.out.println(3/2);	// 1
		System.out.println(3<2);	// false
		System.out.println(3*4);	// 12
		System.out.println(3<<2); 	// 1100(12)
		System.out.println(3*2^2);	// 110^010 = 100(4)
		
		System.out.println(16>4);	// true
		System.out.println(16/4);	// 4
		System.out.println(16>>2);	// 00100(4)
		System.out.println(16/2^2);	// 1000^0010 = 1010(10)
		System.out.println(16>>>2);	// 00100(4)
	}
}
